// ═══════════════════════════════════════════════════════════════
// mapCategory — map free-text category ไปยัง constrained enum
// ═══════════════════════════════════════════════════════════════
/**
 * @param {string} freeText - AI category from Stage 1 or free text
 * @returns {string} constrained Prompt Library category
 */
export function mapCategory(freeText) {
  if (!freeText) return 'ดราม่าสังคม';
  const lower = freeText.toLowerCase().trim();
  
  // Exact match first
  if (CATEGORY_MAP[freeText]) return CATEGORY_MAP[freeText];
  if (CATEGORY_MAP[lower]) return CATEGORY_MAP[lower];
  
  // Partial match
  for (const [key, val] of Object.entries(CATEGORY_MAP)) {
    if (lower.includes(key) || key.includes(lower)) return val;
  }
  
  return 'ดราม่าสังคม'; // default
}


// ═══════════════════════════════════════════════════════════════
// clusterMatch — เทียบ 2 คำ ด้วย semantic cluster
// ═══════════════════════════════════════════════════════════════
/**
 * @param {string} a - คำที่ 1
 * @param {string} b - คำที่ 2 (or single target)
 * @param {object} clusters - EMOTION_CLUSTERS or CONFLICT_CLUSTERS
 * @returns {'exact'|'cluster'|'partial'|false}
 */
export function clusterMatch(a, b, clusters) {
  if (!a || !b) return false;
  const la = a.toLowerCase().trim();
  const lb = b.toLowerCase().trim();
  
  // Exact match
  if (la === lb) return 'exact';
  if (la.includes(lb) || lb.includes(la)) return 'exact';
  
  // Cluster match — ถ้าอยู่ใน cluster เดียวกัน
  for (const [key, members] of Object.entries(clusters)) {
    const membersLower = members.map(m => m.toLowerCase());
    const aInCluster = membersLower.some(m => la.includes(m) || m.includes(la));
    const bInCluster = membersLower.some(m => lb.includes(m) || m.includes(lb));
    if (aInCluster && bInCluster) return 'cluster';
  }
  
  // Partial match — word overlap
  const words1 = la.split(/[\s,\|/]+/).filter(w => w.length > 2);
  const words2 = lb.split(/[\s,\|/]+/).filter(w => w.length > 2);
  if (words1.some(w => lb.includes(w)) || words2.some(w => la.includes(w))) return 'partial';
  
  return false;
}


// ═══════════════════════════════════════════════════════════════
// findClusterScore — หาคะแนนจาก cluster match
// ═══════════════════════════════════════════════════════════════
/**
 * @param {string} tag - tag ที่ต้องหา
 * @param {string[]} targetTags - tags ที่เอามาเทียบ
 * @param {object} clusters - cluster definitions (EMOTION_CLUSTERS or CONFLICT_CLUSTERS)
 * @returns {{matchType: string, score: number}} matchType: 'exact'|'cluster'|'partial'|'none', score: 0-1
 */
export function findClusterScore(tag, targetTags, clusters) {
  if (!tag || !targetTags || targetTags.length === 0) return { matchType: 'none', score: 0 };
  
  let bestMatch = 'none';
  
  for (const target of targetTags) {
    const result = clusterMatch(tag, target, clusters);
    if (result === 'exact') return { matchType: 'exact', score: 1.0 };
    if (result === 'cluster' && bestMatch !== 'exact') bestMatch = 'cluster';
    if (result === 'partial' && bestMatch === 'none') bestMatch = 'partial';
  }
  
  const scoreMap = { exact: 1.0, cluster: 0.75, partial: 0.4, none: 0 };
  return { matchType: bestMatch, score: scoreMap[bestMatch] };
}
