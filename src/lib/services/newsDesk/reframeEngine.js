const HARD_BLOCK = /ฆ่า|ฆาตกรรม|ข่มขืน|ล่วงละเมิด|อนาจารเด็ก|ศพ|ผูกคอ|ฆ่าตัวตาย|กราดยิง|ระเบิดพลีชีพ|จาบจ้วง|หมิ่นสถาบัน|ล้มเจ้า|ยาบ้า|ยาเสพติด|เสพยา|บ่อนพนัน|เว็บพนัน/;

function isHardBlock(item) {
  const text = `${item.title || ''} ${item.snippet || ''}`;
  const g = gateKeywords(item);
  return item.royalNegative === true || item.dramaType === 'hard' || HARD_BLOCK.test(text)
    || (!g.pass && /ฆ่า|ข่มขืน|พนัน|ยา|สถาบัน|ม็อบ|รัฐประหาร/.test(g.reason || ''));
}

// ── เก็บผลลงคลัง 'reframe-cases' (เทส+จริง) — ให้กลับมาเช็กย้อนหลังได้ ──
async function archiveReframe(entry) {
  try {
    const store = createStore('reframe-cases');
    await store.add({ id: randomUUID(), ...entry, at: new Date().toISOString() });
    const all = await store.getAll();
    if (all.length > 300) {
      const old = all.sort((a, b) => new Date(a.at) - new Date(b.at)).slice(0, all.length - 300);
      for (const o of old) await store.remove(o.id).catch(() => {});
    }
  } catch (e) { console.warn('[Reframe] archive fail:', e.message?.slice(0, 40)); }
}

