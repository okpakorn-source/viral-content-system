// ฮาร์ดบล็อก — ห้ามแปลงเด็ดขาด (เป็นข้อความ ไม่ใช่แค่ classify)
const HARD_BLOCK = /ฆ่า|ฆาตกรรม|ข่มขืน|ล่วงละเมิด|อนาจารเด็ก|ศพ|ผูกคอ|ฆ่าตัวตาย|กราดยิง|ระเบิดพลีชีพ|จาบจ้วง|หมิ่นสถาบัน|ล้มเจ้า|ยาบ้า|ยาเสพติด|เสพยา|บ่อนพนัน|เว็บพนัน/;

/**
 * แปลงข่าว 1 ใบ → มุมเชิงบวก 2-4 มุม + brief สะอาด
 * @returns {Promise<{ok, reason?, cleanBrief?, angles?, at?}>}
 */
export async function reframeNews(item) {
  const text = `${item.title || ''} ${item.snippet || ''}`;
  // ── ด่านกัน: ฮาร์ดบล็อก/สถาบันแง่ลบ/ดราม่าหนัก = ห้ามแปลง ──
  const g = gateKeywords(item);
  if (item.royalNegative === true || item.dramaType === 'hard' || HARD_BLOCK.test(text)
    || (!g.pass && /ฆ่า|ข่มขืน|พนัน|ยา|สถาบัน|ม็อบ|รัฐประหาร/.test(g.reason || ''))) {
    return { ok: false, reason: 'ข่าวนี้อ่อนไหว/รุนแรงเกินไป — แปลงมุมไม่ได้ (ฮาร์ดบล็อก)' };
  }

  const angleList = REFRAME_ANGLES.map(a => `- ${a.name}: ${a.cue}`).join('\n');
  const prompt = `คุณเป็นบรรณาธิการเพจข่าวน้ำดี เชี่ยวชาญแปลง "ข่าวกระแส/ดราม่า/ปะทะ/ตรงไปตรงมา" ที่เพจน้ำดีเล่นตรงๆ ไม่ได้ → ให้เป็น "มุมนำเสนอเชิงบวก/ไม่ท็อกซิก" ที่ยังไวรัลได้

=== ข่าว ===
หัวข้อ: ${item.title}
${item.snippet ? 'รายละเอียด: ' + String(item.snippet).slice(0, 600) : ''}
=== จบ ===

เลือก "มุมเชิงบวก" ที่เข้ากับ "บริบทข่าวนี้" ที่สุด 2-4 มุม จากรายการนี้ (เลือกตามบริบท ไม่ต้องครบ):
${angleList}

⛔ กฎเหล็ก:
- ห้ามบิด/โกหกข้อเท็จจริง ห้ามแต่งเติม ห้ามใส่ร้ายฝ่ายตรงข้าม — แค่ "เลือกมุมมองที่ดี" จากข้อเท็จจริงจริง
  ตัวอย่าง: ข่าว "คนดังปะทะกลางรายการเรื่องโกหก" → มุม "เห็นใจ/ชื่นชมคนที่กล้าพูดความจริงเพื่อความเป็นธรรม" (ไม่ใช่ด่าอีกฝ่าย)
- ถ้าข่าวนี้แปลงเป็นบวกไม่ได้จริงๆ (ลบล้วน/อ่อนไหว/ไม่มีมุมดี) ให้ reframable=false

ตอบ JSON เท่านั้น: {
  "reframable": true/false,
  "cleanBrief": "สรุปข้อเท็จจริงของข่าวนี้แบบสะอาด ตัดโทนดราม่า/ฟาด/เร้าออก (2-3 บรรทัด) — พร้อมเป็นวัตถุดิบเขียน",
  "angles": [
    {"type":"ชื่อมุม (จากรายการ)","focus":"โฟกัสที่ใคร + บทบาท (เช่น 'พี่หนุ่ม = คนทวงความจริง')","how":"นำเสนอยังไง เปิดด้วยอะไร เน้นอะไร (≤120 ตัวอักษร)","caption":"ตัวอย่างแคปชั่นเปิดโพสต์เชิงบวก 1 บรรทัด"}
  ]
}`;

  try {
    const res = await callAI({ prompt, model: MODEL_FAST, temperature: 0.6, maxTokens: 1600 });
    const p = typeof res === 'object' ? res : JSON.parse(String(res).match(/\{[\s\S]*\}/)?.[0] || '{}');
    if (!p.reframable || !Array.isArray(p.angles) || !p.angles.length) {
      return { ok: false, reason: 'ข่าวนี้แปลงเป็นมุมบวกไม่ได้ (เป็นลบล้วน/ไม่มีมุมดี)' };
    }
    const angles = p.angles.slice(0, 4).map(a => ({
      type: String(a.type || '').slice(0, 40),
      focus: String(a.focus || '').slice(0, 120),
      how: String(a.how || '').slice(0, 220),
      caption: String(a.caption || '').slice(0, 320),
    })).filter(a => a.how || a.caption);
    if (!angles.length) return { ok: false, reason: 'แปลงมุมได้ไม่ชัดเจน ลองใหม่' };
    return { ok: true, cleanBrief: String(p.cleanBrief || '').slice(0, 900), angles, at: new Date().toISOString() };
  } catch (e) {
    return { ok: false, reason: 'แปลงมุมล้มเหลว: ' + (e.message || '').slice(0, 60) };
  }
}

// ── คัดว่าข่าวใบไหน "ควรแปลงมุม" (ใช้ในโหมดอัตโนมัติ) — ดราม่า/ปะทะ/โทนลบ ที่ไม่ใช่ฮาร์ดบล็อก ──
export function isReframeCandidate(item) {
  if (item.reframe) return false; // แปลงไปแล้ว
  if (item.royalNegative === true || item.dramaType === 'hard') return false;
  if (HARD_BLOCK.test(`${item.title || ''} ${item.snippet || ''}`)) return false;
  // เข้าข่าย: ดราม่านุ่ม / โทนลบ / หมวดดราม่า / ปะทะ-ฟาด
  return item.dramaType === 'soft'
    || item.tone === 'ลบ'
    || /ดราม่า|ปะทะ|ฟาด|โต้กลับ|เคลียร์|ลั่น|ซัด|งัด/.test(item.title || '')
    || ['ดราม่าสังคม', 'คนดัง/ดราม่าบันเทิง'].includes(item.category);
}
