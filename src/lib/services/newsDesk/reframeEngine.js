// ── ประเมินคุณภาพ "เนื้อหาดิบ" ที่แตกประเด็นออกมา (AI judge gpt-5.5) ──
//   วัด 3 อย่าง: (1) ถูกต้องไม่บิดเบือนข้อเท็จจริง (2) แตกประเด็นดี-มุมชัด-ไม่ซ้ำ (3) เป็นวัตถุดิบที่มีศักยภาพไวรัลเมื่อนำไปเจน
export async function evaluateReframe(sourceTitle, result) {
  const anglesText = (result.angles || []).map((a, i) => `${i + 1}. [${a.type}] (โฟกัส: ${a.focus})\n   ${a.rawContent}`).join('\n\n');
  const prompt = `คุณเป็นบรรณาธิการตรวจคุณภาพ "เนื้อหาดิบ/วัตถุดิบข่าว" ก่อนส่งให้ทีมเขียนเอาไป generate เป็นโพสต์ไวรัล
(เนื้อหาดิบที่ดี = ข้อเท็จจริงถูกต้อง ไม่บิดเบือน + แตกประเด็นเป็นมุมเชิงบวกที่ชัดและน่าสนใจ + มีบริบท/เหตุผลครบพอเอาไปขยายเป็นโพสต์ได้)

ข่าวต้นทาง (ข่าวกระแส/ดราม่า): ${sourceTitle}
แก่นข้อเท็จจริง (cleanBrief): ${String(result.cleanBrief || '').slice(0, 500)}

เนื้อหาดิบหลายมุมที่แตกออกมา:
${anglesText}

ประเมินคุณภาพ "เนื้อหาดิบ" ชุดนี้ ให้คะแนน 0-10 โดยดู:
- ความถูกต้อง/ไม่บิดเบือน (ห้ามมีข้อมูลที่ข่าวต้นทางไม่ได้บอก) — สำคัญสุด ถ้าบิดเบือนให้คะแนนต่ำ
- แตกประเด็นดีไหม (มุมชัด ไม่ซ้ำกัน เป็นด้านบวกไม่ท็อกซิก ครอบคลุมแง่มองที่น่าสนใจ)
- เป็นวัตถุดิบที่ "พร้อมเอาไปเจนเป็นโพสต์ไวรัล" แค่ไหน (มีที่มาที่ไป เหตุผล บริบทพอ)
(10 = วัตถุดิบเยี่ยม ถูกต้อง แตกประเด็นคม พร้อมเจนทันที · 5 = พอใช้แต่บางมุมจืด/บาง/ซ้ำ · 0 = บิดเบือน/ใช้ไม่ได้)
ตอบ JSON: {"score":0-10,"why":"เหตุผลสั้นๆ + ถ้ามีจุดบิดเบือน/มุมที่บาง ให้ชี้ชัด + แนะวิธีแตกประเด็นให้ดีขึ้น"}`;
  try {
    const res = await callAI({ prompt, model: MODEL_PRIMARY, temperature: 0.2, maxTokens: 450 });
    const p = typeof res === 'object' ? res : JSON.parse(String(res).match(/\{[\s\S]*\}/)?.[0] || '{}');
    return { score: Math.max(0, Math.min(10, Number(p.score) || 0)), why: String(p.why || '').slice(0, 320) };
  } catch (e) { return { score: 0, why: 'ประเมินไม่สำเร็จ: ' + (e.message || '').slice(0, 50) }; }
}

