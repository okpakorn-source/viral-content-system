        sourceName: item.source || '',
        sources, researchUsed: !!researchBlock,
        // ★ เกณฑ์ที่ใช้คัด/หยิบข่าวนี้ (จากสมองโต๊ะข่าว) — เก็บไว้ให้เห็นว่าทำไมข่าวนี้ถึงเข้าเกณฑ์แต่งประเด็น
