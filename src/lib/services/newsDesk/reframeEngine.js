// ── ประเมิน: มุมที่แปลงได้ "ใกล้บทความไวรัลของเพจน้ำดีไหม" (AI judge gpt-5.5) ──
export async function evaluateReframe(sourceTitle, result) {
  const anglesText = (result.angles || []).map((a, i) => `${i + 1}. [${a.type}] ${a.caption}`).join('\n');
  const prompt = `คุณเป็นนักวิเคราะห์คอนเทนต์ไวรัลของเพจข่าวน้ำดีไทย (รู้ว่าโพสต์ 10k+ รีแอกชันหน้าตาเป็นยังไง: อารมณ์แรง × มอบเงิน/การกุศล/เสียสละ/กตัญญู/ฮีโร่/น้ำใจ + รายละเอียดเล็กที่จริง ชื่อคน ตัวเลข คำพูด)

ข่าวต้นทาง (เป็นข่าวกระแส/ดราม่า): ${sourceTitle}
มุม+แคปชั่นที่ AI แปลงเป็นเชิงบวก:
${anglesText}

ประเมินว่า "มุม/แคปชั่นเหล่านี้ ใกล้เคียงบทความไวรัลของเพจน้ำดีแค่ไหน" ให้คะแนน 0-10
(10 = เหมือนโพสต์ไวรัลจริง ฮุกแรง น่าแชร์ · 5 = พอใช้แต่ยังจืด/สโลแกน · 0 = ไม่เวิร์ก/ยังท็อกซิก)
ตอบ JSON: {"score":0-10,"why":"เหตุผลสั้นๆ ว่าทำไม + จุดที่ควรปรับให้ไวรัลขึ้น"}`;
  try {
    const res = await callAI({ prompt, model: MODEL_PRIMARY, temperature: 0.2, maxTokens: 400 });
    const p = typeof res === 'object' ? res : JSON.parse(String(res).match(/\{[\s\S]*\}/)?.[0] || '{}');
    return { score: Math.max(0, Math.min(10, Number(p.score) || 0)), why: String(p.why || '').slice(0, 300) };
  } catch (e) { return { score: 0, why: 'ประเมินไม่สำเร็จ: ' + (e.message || '').slice(0, 50) }; }
}

