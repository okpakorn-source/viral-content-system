// ── ประเมินคุณภาพ "เนื้อหาดิบเชิงลึก" ที่แตกออกมา (AI judge gpt-5.5) ──
//   วัด: (1) ถูกต้องไม่บิดเบือน (2) แต่ละมุมเล่า "แกนเดียว" ลึก-ครบ ไม่หักมุม/ไม่ปนหลายแกน (3) พร้อมเจนเป็นโพสต์
export async function evaluateReframe(sourceTitle, result) {
  const anglesText = (result.angles || []).map((a, i) => `มุมที่ ${i + 1}. [${a.type}] (โฟกัส: ${a.focus})\n   ${a.rawContent}`).join('\n\n');
  const prompt = `คุณเป็นบรรณาธิการตรวจคุณภาพ "เนื้อหาดิบเชิงลึก" ก่อนส่งให้ทีมเอาไป generate เป็นโพสต์ไวรัล

หลักการที่ทีมต้องการ: "1 ข่าว = 1 มุมที่ดีที่สุด (มากสุด 2 ทางเลือก) เล่าแกนเดียวให้ลึก-ครบที่สุด"
เนื้อหาดิบที่ดี = (ก) ข้อเท็จจริงถูกต้อง ไม่บิดเบือน/ไม่แต่งตัวเลข (ข) แต่ละมุม "อยู่บนแกนอารมณ์/ประเด็นเดียว" ตั้งแต่ต้นจนจบ ไม่สลับไปมา/ไม่หักมุม/ไม่ลากหลายเรื่องมาปน (ค) ลึกพอ ดึงข้อเท็จจริงที่หนุนแกนนั้นมาใช้เต็มที่

ข่าวต้นทาง: ${sourceTitle}
แก่นข้อเท็จจริง: ${String(result.cleanBrief || '').slice(0, 500)}

เนื้อหาดิบ (ควรมี 1-2 มุม แต่ละมุมเป็นแกนเดียวอิสระ):
${anglesText}

ให้คะแนน 0-10 โดยดู:
- ★ "เล่าแกนเดียวลึก" ไหม — ถ้ามุมไหนสลับอารมณ์/หักมุม/ลากหลายประเด็นมาปนในมุมเดียว = หักคะแนนหนัก (ผิดหลักที่ทีมต้องการ)
- ความถูกต้อง/ไม่บิดเบือน (ห้ามมีข้อมูลที่ข่าวไม่ได้บอก) — สำคัญสุด
- ความลึก/ครบ (ดึงข้อเท็จจริงหนุนแกนเต็มที่ ไม่ผ่านๆ) + ถ้ามี >2 มุม หรือมุมซ้ำซ้อน = หักคะแนน
(10 = แกนเดียวคมลึกถูกต้อง พร้อมเจน · 5 = พอใช้แต่ตื้น/เริ่มปนแกน · 0 = บิดเบือน/หลายมุนมั่ว/หักมุมไปมา)
ตอบ JSON: {"score":0-10,"why":"เหตุผลสั้นๆ + ชี้ถ้ามีการปนแกน/หักมุม/บิดเบือน/ตื้น + แนะให้ลึกขึ้น"}`;
  try {
    const res = await callRawJSON({ prompt, model: MODEL_PRIMARY, temperature: 0.2, maxTokens: 450 });
    const p = typeof res === 'object' ? res : JSON.parse(String(res).match(/\{[\s\S]*\}/)?.[0] || '{}');
    return { score: Math.max(0, Math.min(10, Number(p.score) || 0)), why: String(p.why || '').slice(0, 320) };
  } catch (e) { return { score: 0, why: 'ประเมินไม่สำเร็จ: ' + (e.message || '').slice(0, 50) }; }
}

