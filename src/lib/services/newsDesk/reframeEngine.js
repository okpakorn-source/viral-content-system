export async function runReframeTest() {
  const stats = { total: TEST_CASES.length, reframed: 0, blocked: 0, avgScore: 0, cases: [] };
  let sum = 0;
  for (const tc of TEST_CASES) {
    const r = await reframeNews(tc, { mode: 'test', skipArchive: true });
    if (!r.ok) { stats.blocked++; stats.cases.push({ title: tc.title, ok: false, reason: r.reason }); continue; }
    const ev = await evaluateReframe(tc.title, r);
    sum += ev.score; stats.reframed++;
    await archiveReframe({ mode: 'test', sourceTitle: tc.title, sourceSnippet: tc.snippet, cleanBrief: r.cleanBrief, angles: r.angles, evalScore: ev.score, evalNote: ev.why });
    stats.cases.push({ title: tc.title, ok: true, score: ev.score, why: ev.why, angles: r.angles.length });
  }
  stats.avgScore = stats.reframed ? Math.round((sum / stats.reframed) * 10) / 10 : 0;
  return stats;
}

// คัดว่าข่าวใบไหน "ควรแปลงมุม" (ใช้ในโหมดอัตโนมัติ)
export function isReframeCandidate(item) {
  if (item.reframe) return false;
  if (isHardBlock(item)) return false;
  return item.dramaType === 'soft'
    || item.tone === 'ลบ'
    || /ดราม่า|ปะทะ|ฟาด|โต้กลับ|เคลียร์|ลั่น|ซัด|งัด/.test(item.title || '')
    || ['ดราม่าสังคม', 'คนดัง/ดราม่าบันเทิง'].includes(item.category);
}
